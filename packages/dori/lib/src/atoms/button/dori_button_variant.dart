/// Semantic variants for DoriButton.
///
/// Each variant determines the button's visual style and emphasis level.
///
/// {@category Atoms}
enum DoriButtonVariant {
  /// Primary button — high emphasis, filled background.
  ///
  /// Use for the most important action on a screen.
  primary,

  /// Secondary button — medium emphasis, soft background.
  ///
  /// Use for secondary actions that complement the primary action.
  secondary,

  /// Tertiary button — low emphasis, text-only style.
  ///
  /// Use for less prominent actions or as cancel/dismiss buttons.
  tertiary,
}
