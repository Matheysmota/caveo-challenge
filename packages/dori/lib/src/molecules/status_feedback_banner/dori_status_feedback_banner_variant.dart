/// Variants for [DoriStatusFeedbackBanner] component.
///
/// Each variant maps to a semantic color from the feedback palette.
///
/// {@category Molecules}
enum DoriStatusFeedbackBannerVariant {
  /// Success banner for positive feedback.
  ///
  /// Uses success feedback colors (green).
  success,

  /// Error banner for failure states.
  ///
  /// Uses error feedback colors (red).
  error,

  /// Info banner for neutral information.
  ///
  /// Uses info feedback colors (blue).
  info,

  /// Warning banner for cautionary messages.
  ///
  /// Uses warning/info soft colors.
  warning,
}
